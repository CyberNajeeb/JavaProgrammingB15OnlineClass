package day07;

public class CastingMyNameCharacters {

    public static void main(String[] args) {

        char vChar = 'M' + 'o' + 'h' + 'a' + 'm' + 'm'+ 'e' + 'd';
        char aChar = 'N' + 'a' + 'j' + 'e' + 'e' + 'b';
        char cChar = 'S' + 'o' + 'h' + 'r' + 'a' + 'b'+ 'i';
        int eChar = vChar;
        int bChar = aChar;
        int dChar = cChar;

        System.out.println(eChar);
        System.out.println(bChar);
        System.out.println(dChar);

    }
}
