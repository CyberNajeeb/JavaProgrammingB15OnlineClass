package day06;

public class CompoundOperator  {

    public static void main(String[] args) {


        int studentsOnline = 263;

        studentsOnline += 5;

        System.out.println(studentsOnline);

        studentsOnline -= 3;

        System.out.println(studentsOnline);

        studentsOnline *= 2;

        System.out.println(studentsOnline);

        studentsOnline = studentsOnline + studentsOnline;

        System.out.println(studentsOnline);

        studentsOnline /= 3;

        System.out.println(studentsOnline);




    }
}
