package day14;

public class StringMethodCharAt {

    public static void main(String[] args) {

        String name = "Najeeb";

        System.out.print(name.toUpperCase().charAt(0));
        System.out.print(name.toUpperCase().charAt(1));
        System.out.print(name.toUpperCase().charAt(2));
        System.out.print(name.toUpperCase().charAt(3));
        System.out.print(name.toUpperCase().charAt(4));
        System.out.print(name.toUpperCase().charAt(5));
    }
}
